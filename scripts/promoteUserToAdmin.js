#!/usr/bin/env node

const admin = require('firebase-admin');
const { getAuth } = require('firebase-admin/auth');
const axios = require('axios');

/**
 * Script para promover um usu√°rio a system_admin
 * Identifica o usu√°rio por email e atribui privil√©gios administrativos
 */

const BASE_URL = 'https://us-central1-curva-mestra.cloudfunctions.net';

// Inicializar Firebase Admin
if (!admin.apps.length) {
  try {
    // Tentar usar Application Default Credentials primeiro
    admin.initializeApp({
      projectId: 'curva-mestra',
      credential: admin.credential.applicationDefault()
    });
  } catch (error) {
    console.log('‚ö†Ô∏è  Application Default Credentials n√£o dispon√≠veis, tentando sem credenciais...');
    // Fallback para inicializa√ß√£o b√°sica (funciona em alguns casos)
    admin.initializeApp({
      projectId: 'curva-mestra'
    });
  }
}

class UserPromoter {
  constructor() {
    this.adminToken = null;
  }

  async generateAdminToken() {
    try {
      console.log('üîë Gerando token do admin padr√£o...');
      const adminUID = 'gEjUSOsHF9QmS0Dvi0zB10GsxrD2';
      this.adminToken = await getAuth().createCustomToken(adminUID);
      console.log('‚úÖ Token do admin gerado com sucesso');
      return this.adminToken;
    } catch (error) {
      console.error('‚ùå Erro ao gerar token do admin:', error.message);
      throw error;
    }
  }

  async findUserByEmail(email) {
    try {
      console.log(`üîç Procurando usu√°rio: ${email}`);
      const userRecord = await getAuth().getUserByEmail(email);
      console.log(`‚úÖ Usu√°rio encontrado:`);
      console.log(`   UID: ${userRecord.uid}`);
      console.log(`   Email: ${userRecord.email}`);
      console.log(`   Nome: ${userRecord.displayName || 'N√£o definido'}`);
      return userRecord;
    } catch (error) {
      if (error.code === 'auth/user-not-found') {
        console.error(`‚ùå Usu√°rio n√£o encontrado: ${email}`);
        console.log('üí° Certifique-se de que o usu√°rio est√° registrado no Firebase Auth');
        return null;
      }
      console.error('‚ùå Erro ao buscar usu√°rio:', error.message);
      throw error;
    }
  }

  async getCurrentUserClaims(uid) {
    try {
      console.log(`üîç Verificando claims atuais do usu√°rio: ${uid}`);
      const userRecord = await getAuth().getUser(uid);
      const claims = userRecord.customClaims || {};
      
      console.log('üìã Claims atuais:');
      console.log(JSON.stringify(claims, null, 2));
      
      return claims;
    } catch (error) {
      console.error('‚ùå Erro ao obter claims:', error.message);
      throw error;
    }
  }

  async callAdminAPI(endpoint, data = {}) {
    const url = `${BASE_URL}/${endpoint}`;
    const headers = {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${this.adminToken}`
    };

    const payload = { data };

    try {
      console.log(`üöÄ Chamando API: ${endpoint}`);
      console.log(`üì§ Dados:`, JSON.stringify(data, null, 2));
      
      const response = await axios.post(url, payload, { headers });
      
      console.log(`‚úÖ Sucesso (${response.status})`);
      console.log(`üì• Resposta:`, JSON.stringify(response.data, null, 2));
      
      return response.data;
    } catch (error) {
      console.error(`‚ùå Erro na API ${endpoint}:`);
      if (error.response) {
        console.error(`Status: ${error.response.status}`);
        console.error(`Resposta:`, JSON.stringify(error.response.data, null, 2));
      } else {
        console.error(`Erro:`, error.message);
      }
      throw error;
    }
  }

  async promoteUserToAdmin(email) {
    try {
      console.log(`\nüöÄ === PROMOVENDO USU√ÅRIO A SYSTEM_ADMIN ===`);
      console.log(`üìß Email: ${email}\n`);

      // 1. Gerar token do admin
      await this.generateAdminToken();

      // 2. Encontrar o usu√°rio
      const user = await this.findUserByEmail(email);
      if (!user) {
        throw new Error('Usu√°rio n√£o encontrado');
      }

      // 3. Verificar claims atuais
      console.log('\nüìã === VERIFICANDO STATUS ATUAL ===');
      await this.getCurrentUserClaims(user.uid);

      // 4. Verificar se j√° √© admin via API
      console.log('\nüîç === VERIFICANDO VIA API ===');
      try {
        await this.callAdminAPI('getUserRole', { uid: user.uid });
      } catch (error) {
        console.log('‚ÑπÔ∏è  Usu√°rio ainda n√£o tem role definido via API');
      }

      // 5. Promover a admin
      console.log('\nüëë === PROMOVENDO A ADMIN ===');
      await this.callAdminAPI('setAdminRole', { uid: user.uid });

      // 6. Verificar se funcionou
      console.log('\n‚úÖ === VERIFICANDO PROMO√á√ÉO ===');
      await this.callAdminAPI('getUserRole', { uid: user.uid });
      await this.callAdminAPI('verifyAdminRole', { uid: user.uid });

      // 7. Verificar claims finais
      console.log('\nüìã === CLAIMS FINAIS ===');
      await this.getCurrentUserClaims(user.uid);

      console.log(`\nüéâ === PROMO√á√ÉO CONCLU√çDA COM SUCESSO ===`);
      console.log(`‚úÖ ${email} agora √© system_admin!`);
      console.log(`üîë UID: ${user.uid}`);

      return {
        success: true,
        uid: user.uid,
        email: user.email,
        message: 'Usu√°rio promovido a system_admin com sucesso'
      };

    } catch (error) {
      console.error('\nüí• === ERRO NA PROMO√á√ÉO ===');
      console.error(error.message);
      throw error;
    }
  }

  async listAllUsers() {
    try {
      console.log('\nüë• === LISTANDO TODOS OS USU√ÅRIOS ===');
      
      const listUsersResult = await getAuth().listUsers(1000);
      
      console.log(`üìä Total de usu√°rios: ${listUsersResult.users.length}\n`);
      
      for (const user of listUsersResult.users) {
        const claims = user.customClaims || {};
        const isAdmin = claims.admin || false;
        const role = claims.role || 'user';
        
        console.log(`üë§ ${user.email || 'Email n√£o definido'}`);
        console.log(`   UID: ${user.uid}`);
        console.log(`   Nome: ${user.displayName || 'N√£o definido'}`);
        console.log(`   Role: ${role}`);
        console.log(`   Admin: ${isAdmin ? '‚úÖ' : '‚ùå'}`);
        console.log(`   Verificado: ${user.emailVerified ? '‚úÖ' : '‚ùå'}`);
        console.log(`   Criado: ${user.metadata.creationTime}`);
        console.log('');
      }
      
      return listUsersResult.users;
    } catch (error) {
      console.error('‚ùå Erro ao listar usu√°rios:', error.message);
      throw error;
    }
  }

  async initializeSystem() {
    try {
      console.log('\nüîß === INICIALIZANDO SISTEMA ===');
      
      const url = `${BASE_URL}/initializeDefaultAdmin`;
      const response = await axios.post(url, { data: {} }, {
        headers: { 'Content-Type': 'application/json' }
      });
      
      console.log('‚úÖ Sistema inicializado:');
      console.log(JSON.stringify(response.data, null, 2));
      
      return response.data;
    } catch (error) {
      console.error('‚ùå Erro ao inicializar sistema:', error.message);
      if (error.response) {
        console.error('Resposta:', JSON.stringify(error.response.data, null, 2));
      }
      throw error;
    }
  }
}

async function main() {
  const args = process.argv.slice(2);
  const command = args[0];
  const email = args[1];
  
  const promoter = new UserPromoter();
  
  try {
    switch (command) {
      case 'promote':
        if (!email) {
          console.error('‚ùå Email √© obrigat√≥rio para promo√ß√£o');
          console.log('üí° Uso: node promoteUserToAdmin.js promote <email>');
          process.exit(1);
        }
        await promoter.promoteUserToAdmin(email);
        break;
        
      case 'list':
        await promoter.listAllUsers();
        break;
        
      case 'init':
        await promoter.initializeSystem();
        break;
        
      case 'find':
        if (!email) {
          console.error('‚ùå Email √© obrigat√≥rio para busca');
          console.log('üí° Uso: node promoteUserToAdmin.js find <email>');
          process.exit(1);
        }
        await promoter.findUserByEmail(email);
        break;
        
      default:
        console.log(`
üöÄ User Promoter - Curva Mestra

Comandos dispon√≠veis:

üëë Promover usu√°rio a system_admin:
   node promoteUserToAdmin.js promote <email>

üë• Listar todos os usu√°rios:
   node promoteUserToAdmin.js list

üîç Encontrar usu√°rio por email:
   node promoteUserToAdmin.js find <email>

üîß Inicializar sistema:
   node promoteUserToAdmin.js init

Exemplos:
   node promoteUserToAdmin.js promote usuario@exemplo.com
   node promoteUserToAdmin.js list
   node promoteUserToAdmin.js find usuario@exemplo.com
   node promoteUserToAdmin.js init
        `);
        break;
    }
  } catch (error) {
    console.error('üí• Erro na execu√ß√£o:', error.message);
    process.exit(1);
  }
}

if (require.main === module) {
  main();
}

module.exports = UserPromoter;